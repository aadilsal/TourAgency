export const updateDestination = mutation({
  args: {
    destinationId: v.id("destinations"),
    name: v.optional(v.string()),
    slug: v.optional(v.string()),
    line: v.optional(v.string()),
    description: v.optional(v.string()),
    heroExternalUrl: v.optional(v.string()),
    matchTerms: v.optional(v.array(v.string())),
    bestTime: v.optional(v.string()),
    tips: v.optional(v.array(v.string())),
    costEstimate: v.optional(v.string()),
    bullets: v.optional(v.array(v.string())),
    sortOrder: v.optional(v.number()),
  },
  handler: async (ctx, { destinationId, ...patch }) => {
    await requireAdmin(ctx);
    const existing = await ctx.db.get(destinationId);
    if (!existing) throw new Error("Destination not found");

    const next: Record<string, unknown> = {};
    for (const [k, val] of Object.entries(patch)) {
      if (val !== undefined) next[k] = val;
    }

    if (typeof next.slug === "string") {
      const normalized = String(next.slug).trim().toLowerCase();
      if (normalized && normalized !== existing.slug) {
        const conflict = await ctx.db
          .query("destinations")
          .withIndex("by_slug", (q) => q.eq("slug", normalized))
          .unique();
        if (conflict && conflict._id !== destinationId) {
          throw new Error("Slug already exists");
        }
      }
      next.slug = normalized;
    }

    if (Array.isArray(next.matchTerms)) {
      next.matchTerms = (next.matchTerms as string[])
        .map((t) => t.trim().toLowerCase())
        .filter(Boolean);
    }
    if (Array.isArray(next.tips)) {
      next.tips = (next.tips as string[]).map((t) => t.trim()).filter(Boolean);
    }
    if (Array.isArray(next.bullets)) {
      next.bullets = (next.bullets as string[]).map((b) => b.trim()).filter(Boolean);
    }

    next.updatedAt = Date.now();

    await ctx.db.patch(
      destinationId,
      next as Partial<Omit<Doc<"destinations">, "_id" | "_creationTime">>,
    );
  },
});

export const deleteDestination = mutation({
  args: { destinationId: v.id("destinations") },
  handler: async (ctx, { destinationId }) => {
    await requireAdmin(ctx);
    const tours = await ctx.db.query("tours").collect();
    for (const t of tours) {
      const nextDestinationIds = Array.isArray(t.destinationIds)
        ? t.destinationIds.filter((id) => id !== destinationId)
        : undefined;
      const patch: Record<string, unknown> = {};
      if (t.destinationId === destinationId) {
        patch.destinationId = undefined;
      }
      if (Array.isArray(t.destinationIds) && t.destinationIds.includes(destinationId)) {
        patch.destinationIds = nextDestinationIds;
      }
      if (Object.keys(patch).length > 0) {
        await ctx.db.patch(t._id, patch as Partial<Doc<"tours">>);
      }
    }
    await ctx.db.delete(destinationId);
  },
});

export const syncFromTour = internalMutation({
  args: { tourId: v.id("tours") },
  handler: async (ctx, { tourId }) => {
    const tour = await ctx.db.get(tourId);
    if (!tour || !tour.isActive) return;

    const dests = await ctx.db.query("destinations").collect();
    const primary = primaryVisualFromImages(tour.images);
    const now = Date.now();
    const assignedDestinationIds = [
      ...(Array.isArray(tour.destinationIds) ? tour.destinationIds : []),
      ...(tour.destinationId ? [tour.destinationId] : []),
    ];

    if (assignedDestinationIds.length > 0) {
      const uniqueAssigned = Array.from(new Set(assignedDestinationIds));
      for (const destinationId of uniqueAssigned) {
        const assigned = await ctx.db.get(destinationId);
        if (!assigned) continue;
        const shouldSetHero =
          assigned.autoGenerated ||
          (!assigned.heroStorageId && !assigned.heroExternalUrl);
        if (shouldSetHero) {
          await ctx.db.patch(assigned._id, {
            ...primary,
            updatedAt: now,
          });
        }
      }
      return;
    }

    const matched = dests.filter((d) =>
      locationMatchesTerms(tour.location, d.matchTerms),
    );

    if (matched.length > 0) {
      for (const d of matched) {
        const shouldSetHero =
          d.autoGenerated ||
          (!d.heroStorageId && !d.heroExternalUrl);
        if (!shouldSetHero) continue;
        await ctx.db.patch(d._id, {
          ...primary,
          updatedAt: now,
        });
      }
      return;
    }

    const base = slugifyLocation(tour.location);
    const slug = await ensureUniqueSlug(ctx, base);
    await ctx.db.insert("destinations", {
      slug,
      name: readableName(tour.location),
      line: "Tours and experiences in this region.",
      description: `Browse active tours and packages covering ${tour.location}.`,
      matchTerms: matchTermsFromLocation(tour.location, slug),
      bestTime:
        "Contact us for the best seasonal windows and road status for this area.",
      tips: [
        "Book transport and stays early in peak season.",
        "Confirm road and pass status before travel.",
      ],
      costEstimate:
        "Pricing varies by group size and inclusions — see individual tours in this region.",
      bullets: [
        "Hand-picked routes from our catalog.",
        "Combine with nearby regions for longer trips.",
      ],
      autoGenerated: true,
      sortOrder: 1000,
      createdAt: now,
      updatedAt: now,
      ...primary,
    });
  },
});

export const listForIndex = query({
  args: {},
  handler: async (ctx) => {
    const dests = await ctx.db.query("destinations").collect();
    if (dests.length === 0) return [];
    const active = await ctx.db
      .query("tours")
      .withIndex("by_isActive", (q) => q.eq("isActive", true))
      .take(500);
    const sorted = dests.sort(
      (a, b) => a.sortOrder - b.sortOrder || a.name.localeCompare(b.name),
    );
    return Promise.all(
      sorted.map(async (d) => {
        const matched = active.filter((t) =>
          belongsToDestination(t, d),
        );
        const heroUrl = await resolveDestinationHeroUrl(ctx, d);
        const preview = await Promise.all(
          matched.slice(0, 3).map(async (t) => ({
            slug: t.slug,
            title: t.title,
            price: t.price,
            images: await resolveTourImageUrls(ctx, t.images),
          })),
        );
        return {
          slug: d.slug,
          name: d.name,
          line: d.line,
          heroUrl,
          tourCount: matched.length,
          previewTours: preview,
          moreCount: Math.max(0, matched.length - 3),
        };
      }),
    );
  },
});

