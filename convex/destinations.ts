export const bulkUpsert = mutation({
  args: {
    rows: v.array(
      v.object({
        slug: v.optional(v.string()),
        name: v.string(),
        line: v.string(),
        description: v.string(),
        heroExternalUrl: v.optional(v.string()),
        matchTerms: v.array(v.string()),
        bestTime: v.string(),
        tips: v.array(v.string()),
        costEstimate: v.string(),
        bullets: v.array(v.string()),
        sortOrder: v.optional(v.number()),
      }),
    ),
  },
  handler: async (ctx, { rows }) => {
    const admin = await requireAdmin(ctx);
    const now = Date.now();
    let created = 0;
    let updated = 0;
    let skipped = 0;
    const errors: Array<{ index: number; message: string }> = [];

    for (let i = 0; i < rows.length; i++) {
      const row = rows[i]!;
      try {
        const base = (row.slug?.trim() || slugifyLocation(row.name)).toLowerCase();
        if (!row.name.trim()) throw new Error("name is required");
        if (!row.line.trim()) throw new Error("line is required");
        if (!row.description.trim()) throw new Error("description is required");
        if (!row.bestTime.trim()) throw new Error("bestTime is required");
        if (!row.costEstimate.trim()) throw new Error("costEstimate is required");

        const normalizedSlug = base;
        const existing = await ctx.db
          .query("destinations")
          .withIndex("by_slug", (q) => q.eq("slug", normalizedSlug))
          .unique();

        const payload = {
          slug: normalizedSlug,
          name: row.name.trim(),
          line: row.line.trim(),
          description: row.description.trim(),
          heroExternalUrl: row.heroExternalUrl?.trim() || undefined,
          matchTerms: row.matchTerms.map((t) => t.trim().toLowerCase()).filter(Boolean),
          bestTime: row.bestTime.trim(),
          tips: row.tips.map((t) => t.trim()).filter(Boolean),
          costEstimate: row.costEstimate.trim(),
          bullets: row.bullets.map((b) => b.trim()).filter(Boolean),
          sortOrder: row.sortOrder ?? 100,
          updatedAt: now + i,
        };

        if (existing) {
          await ctx.db.patch(existing._id, payload);
          updated++;
        } else {
          const slug = await ensureUniqueSlug(ctx, normalizedSlug);
          await ctx.db.insert("destinations", {
            ...payload,
            slug,
            autoGenerated: false,
            createdAt: now + i,
          });
          created++;
        }
      } catch (e) {
        errors.push({ index: i, message: e instanceof Error ? e.message : String(e) });
        skipped++;
      }
    }

    await ctx.db.insert("adminLogs", {
      action: "bulk_upsert_destinations",
      performedBy: admin._id,
      timestamp: Date.now(),
      details: `processed=${rows.length} created=${created} updated=${updated} skipped=${skipped}`,
    });

    return { processed: rows.length, created, updated, skipped, errors };
  },
});
