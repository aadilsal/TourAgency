const FALLBACK_HERO =
  "https://images.unsplash.com/photo-1506905925346-21bda4d32df4?auto=format&fit=crop&w=2000&q=80";

function locationMatchesTerms(location: string, terms: string[]): boolean {
  const l = location.toLowerCase();
  return terms.some((t) => t.length >= 2 && l.includes(t.toLowerCase()));
}

function slugifyLocation(loc: string): string {
  const first = loc.split(/[,·|]/)[0]?.trim() || loc.trim();
  const s = first
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 72);
  return s || "destination";
}

function readableName(location: string): string {
  const first = location.split(/[,·|]/)[0]?.trim() || location.trim();
  return first.slice(0, 80) || "Destination";
}

function belongsToDestination(
  tour: Doc<"tours">,
  destination: Doc<"destinations">,
): boolean {
  if (Array.isArray(tour.destinationIds) && tour.destinationIds.length > 0) {
    return tour.destinationIds.some((id) => id === destination._id);
  }
  if (tour.destinationId) {
    return tour.destinationId === destination._id;
  }
  return locationMatchesTerms(tour.location, destination.matchTerms);
}

function matchTermsFromLocation(loc: string, slug: string): string[] {
  const terms = new Set<string>([slug.toLowerCase()]);
  for (const part of loc.toLowerCase().split(/[,·\s/|]+/)) {
    const p = part.trim();
    if (p.length >= 3) terms.add(p);
  }
  terms.add(slugifyLocation(loc));
  return Array.from(terms);
}

function primaryVisualFromImages(images: string[]): {
  heroStorageId?: Id<"_storage">;
  heroExternalUrl?: string;
} {
  for (const r of images) {
    const t = r.trim();
    if (!t) continue;
    if (/^https?:\/\//i.test(t)) return { heroExternalUrl: t };
    if (t.startsWith("/")) return { heroExternalUrl: t };
    return { heroStorageId: t as Id<"_storage"> };
  }
  return {};
}

export async function resolveDestinationHeroUrl(
  ctx: { storage: { getUrl: (id: Id<"_storage">) => Promise<string | null> } },
  d: Doc<"destinations">,
): Promise<string> {
  if (d.heroStorageId) {
    const u = await ctx.storage.getUrl(d.heroStorageId);
    if (u) return u;
  }
  if (d.heroExternalUrl) return d.heroExternalUrl;
  return FALLBACK_HERO;
}

async function ensureUniqueSlug(ctx: MutationCtx, base: string): Promise<string> {
  let slug = base;
  let n = 0;
  for (;;) {
    const existing = await ctx.db
      .query("destinations")
      .withIndex("by_slug", (q) => q.eq("slug", slug))
      .unique();
    if (!existing) return slug;
    n += 1;
    slug = `${base}-${n}`;
  }
}

